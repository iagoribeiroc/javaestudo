public class Comparativos {

  public static void main(String[] args) throws Exception {
    int n1 = 5;
    int n2 = 9;

    String nome1 = "Iago";
    String nome2 = "Gabrielle";

    System.out.println("n1 = n2? " + (n1 == n2));
    System.out.println("n1 > n2? " + (n1 > n2));
    System.out.println("n1 < n2? " + (n1 < n2));
    System.out.println("n1 != n2? " + (n1 != n2));
    System.out.println("nome1 = nome2? " + (nome1 == nome2));
  }
}
